    /**
     * al generar la cadena del grafo, esta se puede repetir, para eso uso esta lista
     */
    ArrayList<String> added = new ArrayList<String>();
    /**
     * esta lista sirve para añadir los identificadores de los nodos, así cuando cuento los nodos, uso una función hash
     */
    HashSet<Integer> labels = new HashSet<Integer>();
    /**
     * realizo una busqueda en profundidad para obtener las cadenas para el graphviz
     * @param n 
     */
    public void explore(CNode n) {
        if (n.m_pLeftNode != null && n.m_pRightNode == null && !n.m_GExplored) {
            String m = "" + n.getId()
                    + " " + n.m_pLeftNode.getId() + "\n";
            labels.add(n.getId());
            labels.add(n.m_pLeftNode.getId());
            boolean band = false;
            /*checo que no se repitan*/
            for (int i = 0; i < added.size(); i++) {
                if (m.equals(added.get(i))) {
                    band = true;
                    break;
                }
            }
            added.add(m);
            if (!band) {
                mm = mm + m;
                msj = msj + "\"" + n.getId() + ": " + n.getSingleCodeLine().replaceAll("\"", "``") + "\"" + "->" + "\"" + n.m_pLeftNode.getId() + ": " + n.m_pLeftNode.getSingleCodeLine().replaceAll("\"", "``") + "\"\n";
            }
            n.m_GExplored = true;
            explore(n.m_pLeftNode);
        }
        if (n.m_pRightNode != null) {
            String m = "" + n.getId()
                    + " " + n.m_pLeftNode.getId() + " " + n.m_pRightNode.getId() + "\n";
            labels.add(n.getId());
            labels.add(n.m_pRightNode.getId());
            boolean band = false;
            for (int i = 0; i < added.size(); i++) {
                if (m.equals(added.get(i))) {
                    band = true;
                    break;
                }
            }
            added.add(m);
            if (!band) {
                msj = msj + "\"" + n.getId() + ": " + n.getSingleCodeLine().replaceAll("\"", "``") + "\"" + "->" + "\"" + n.m_pLeftNode.getId() + ": " + n.m_pLeftNode.getSingleCodeLine().replaceAll("\"", "``") + "\"\n";
                msj = msj + "\"" + n.getId() + ": " + n.getSingleCodeLine().replaceAll("\"", "``") + "\"" + "->" + "\"" + n.m_pRightNode.getId() + ": " + n.m_pRightNode.getSingleCodeLine().replaceAll("\"", "``") + "\"\n";
                mm = mm + m;
            }
            n.m_GExplored = true;
            explore(n.m_pLeftNode);
            explore(n.m_pRightNode);
        }

    }

    /**
     * ESCRIBIR EN UN FICHERO
     *
     * @param name
     * @param cont
     */
    public void write(String name, String cont) {
        FileWriter fichero = null;
        BufferedWriter pw = null;
        try {
            fichero = new FileWriter(System.getProperty("user.dir") + name + ".txt");
            pw = new BufferedWriter(fichero);
            pw.write(cont);
            pw.close();

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (null != fichero) {
                    fichero.close();
                }
            } catch (Exception e2) {
                e2.printStackTrace();
            }
        }
    }
